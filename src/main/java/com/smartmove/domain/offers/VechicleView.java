public class VechicleView {

    private String display_name;
    private String id;
    private String product_display_type;
    
    public String getDisplay_name() {
        return display_name;
    }
    
    public void setDisplay_name(String display_name) {
        this.display_name = display_name;
    }
    
    public String getId() {
        return id;
    }
    
    public void setId(String id) {
        this.id = id;
    }
    
    public String getProduct_display_type() {
        return product_display_type;
    }
    
    public void setProduct_display_type(String product_display_type) {
        this.product_display_type = product_display_type;
    }
}
